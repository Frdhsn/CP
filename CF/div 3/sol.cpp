ll nC3(ll n){
    if(n<3)return 0;
    ll a = n;
    ll b = n-1;
    ll c = n-2;
    ll res = 1;
    if(a%3==0)         {res = a/3;res*=b;res*=c;}
    else if(b%3==0){res = b/3;res*=a;res*=c;}
    else if(c%3==0){res = c/3;res*=a;res*=b;}
    res = res/2;
    return res;
}
