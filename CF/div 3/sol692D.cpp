ll dfs(int idx,int zero,ll zero_cnt,ll one_cnt){
    if(idx>=n)return 0;
    //if(dp[idx][zero]!=-1)return dp[idx][zero];
    //cout<<idx<<" "<<zero<<" "<<zero_cnt<<" "<<one_cnt<<endl;
    ll ret=0;
    if(s[idx]=='1'){
        //one_cnt++;
        zero=0;
        ret = zero_cnt*x;
        ret = ret + dfs(idx+1,0,zero_cnt,one_cnt+1);
    }
    else if(s[idx]=='0'){
        zero=1;
        ret = one_cnt*y;
        ret = ret + dfs(idx+1,1,zero_cnt+1,one_cnt);
    }
    else{
        zero = 2;
        ll xx = dfs(idx+1,1,zero_cnt+1,one_cnt) + one_cnt*y;  // zero boshacchi
        dp[idx][1]=xx;
        ll yy = dfs(idx+1,0,zero_cnt,one_cnt+1) + zero_cnt*x; // one boshacchi
        dp[idx][0]=yy;

        ret=min(xx,yy);
    }
    return dp[idx][zero]=ret;
}
