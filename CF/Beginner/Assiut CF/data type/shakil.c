// s = d/t
#include<stdio.h>
int main () {
	float s, d, t;
	printf("Enter d = ");
	scanf("%f", &d);
	printf("Enter t = ");
	scanf("%f", &t);
	s = d / t;
	printf("s = %f\n", s);
	return 0;
}
